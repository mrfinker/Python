### Exposant
# Deux asterisques ** est utilisés pour elever un nombre à la puissance d'un autre appeler exponentiation

print( 2**4 )
print()

# On peut mettre une valeur a une autre puissance consecutive
# on peut utiliser le floatteur pour les exposants

print( 2**3**2 )
print()

### Le code suivant est la racine carree de 9

print( 9 ** ( 1 / 2 ))
print()

#------#

print( 1.5 ** 5)
print(8 ** ( 1 / 3 ))
print()

# Si nous voulons une division entiere appeler quotient, nous utiliserons (// double slash)

print( 20//6 )
print( 10//4 )
print()

# Si nous voulons seulement le reste de la division, nous utiliserons l'operateur ( % )

print(20 % 6)
print( 1024 % 42 )
print( 7 % (5 // 2))
print()

# La meme chose avec les flotteurs

print( 1.25 % 0.5 )
print( 4.2 // 1.4 )
print( 6 // 1.5 )
print()