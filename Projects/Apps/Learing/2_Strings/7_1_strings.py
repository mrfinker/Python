# Parlons mieux des Strings ou appeler communement chaine de caractere
# Ceci n'affecte en rien son comportement

print("Bonjour")
print('Bienvenue')
print()

# Tout ce qui est entre quotes est une chaine de caractere meme les valeurs entieres ou flotantes
# ces sont des string pas de des entiers

print("45")
print('14')
print()

## Le Backslash en python \ est un caractere special appeler ( escape charactere )
# Il est utiliser pour representer certaines choses dans une chaine de caractere comme :
# une nouvelle ligne, un onglet et bien d'autres
# on l'utilise aussi pour inclure des strings dans des quotes

print('La mere de Ruth : elle n\'est pas un ange. C\'est un vilain garçon.')
print()

# Nous pouvons faire la meme chose avec le double quotes

print("Il a dit : \"bien joué\".")
print()