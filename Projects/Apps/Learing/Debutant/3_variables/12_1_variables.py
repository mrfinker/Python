## LEs variables
# La variables permet de stocker une valeur en l'attribuant à un nom
# ainsi le nom peut etre utiliser pour ulterieurement dans le programme
# par exemple stocker le nombre des points du jeu d'un utilisateur
# Pour attribuer une variable nous utilisons le signe egal ( = )

utilisateur = "Gift"

# ici nous avons assigner une chaine de caractere Gift a la variable utilisateur
# Le signe egal est un operateur d'affectation et est utiliser pour attribuer une valeur à une variable
# Nous pouvons attribuer n'importe quel type de valeur à votre une variable

x = 168
j = "Bonjour"
age = 41
s = 3.5569

