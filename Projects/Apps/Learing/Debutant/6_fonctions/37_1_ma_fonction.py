"""
Vous pouvez créer vos propres fonctions en utilisant l'instruction def.

Voici un exemple de fonction nommée my_func. Il ne prend aucun argument et affiche "faim" trois fois.
"""
def my_func():
    print("faim")
    print("faim")
    print("faim")

def hello():
    print("Salut !")

"""
Tout comme avec les instructions if, le bloc de code dans chaque fonction commence par deux points (:) et est indenté.
"""

def ma_fonction():
    print("faim")
    print("faim_2")
    print("faim_3")

"""
Ainsi, une fois que vous avez défini une fonction, vous pouvez l'appeler plusieurs fois dans votre code.
Notez que nous appelons une fonction en utilisant son nom et les parenthèses.
Comme ça:
"""
def bonjour():
    print("Bonjour le monde!")

bonjour()
bonjour()
bonjour()
print()

def salut():
    print("Salut")

salut()
print()

