"""
Les fonctions

Une fonction est un groupe d'instructions liées qui exécutent une tâche spécifique.
Les fonctions aident à diviser notre programme en morceaux plus petits et modulaires. Au fur et à mesure que notre programme grandit, les fonctions le rendent plus organisé et plus gérable.
De plus, cela évite les répétitions et rend le code réutilisable.

Une fonction a un nom et peut avoir des arguments.
Ici, print est le nom de la fonction et "Hello" est l'argument.
"""

print("Bonjour")

"""
Il existe de nombreuses fonctions intégrées dans Python.
Pour vous rafraîchir la mémoire, voici quelques exemples que nous avons déjà vus:
"""

print("Hello, word")
range(2, 82)
str(15)
range(10, 20, 4)

"""
Les fonctions peuvent avoir plusieurs arguments.
Par exemple, range(2, 20) a deux arguments, 2 et 20.
Les arguments de la fonction doivent être séparés par des virgules.
"""

# combien d'arguments exist-il dans cette fonction
range(10, 20, 4) # 3 arguments

"""
Vous appelez la fonction en utilisant son nom, suivi de parenthèses, qui entourent les arguments.
Par exemple, nous appelons la fonction print() avec un argument de chaîne pour générer une sortie:
"""
print("un text")